/// <summary>
/// Shortcut for Unity input axes.
/// </summary>
public static class Axis
{
    /// <summary>
    /// Horizontal axis.
    /// </summary>
    public const string X = "Horizontal";

    /// <summary>
    /// Vertical axis.
    /// </summary>
    public const string Y = "Vertical";

    /// <summary>
    /// Mouse horizontal axis.
    /// </summary>
    public const string MOUSE_X = "Mouse X";

    /// <summary>
    /// Mouse vertical axis.
    /// </summary>
    public const string MOUSE_Y = "Mouse Y";
}